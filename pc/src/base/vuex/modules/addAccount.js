//总校设置=》账=》添加账号
const addAccount = {
	state:{
		//控制新增数据后同步修改列表展示
		addRoleNum : 0,
		//账号修改角色，回显展示
	    addperson : "",
	    //账号显示已经选中的人
	    showChoose:{},
	    //当前编辑的索引
	    nowIndex:0,
	},
	mutations:{
		SET_ADDPERSON_NUM(state,data){
			state.addRoleNum = data
		},
		SET_ADDPERSON(state , data){
		state.addperson = data
		},
		SET_CHOOSE(state , data){
			state.showChoose = data
		},
		SET_NOWINDEX(state,data){
			state.nowIndex=data
		}
	}
}

export default addAccount